def main():
    grade = int(input("Enter grade(1-5):"))
    if grade == 1:
        print("Fail")
    elif grade == 2:
        print("Poor")
    elif grade == 3:
        print("Fair")
    elif grade == 4:
        print("Good")
    elif grade == 5:
        print("Excellent")
    else:
        print("Please enter a value between 1-5")

main()
                      
